# Arithmetic operators

a = 5
b = 6
c = a + b
print(c)

# Assignment operators

d = 5 - 3
print(d)

f = 4
f+=3  # This means increase the value of f by 3 (Increment )
print(f)
f-=2 # This means decrease the value of f by 2 (Decrement)
print(f)


# Comparison operaators

g = 5<2
print(g)

h = 5>2
print(h)

i = 5!=5
print(i)

j = 5==5
print(j)

# Logical operators

k = True or False
print(k)
print("=============================================")
# OR truth table 

print(f"True or Flase is : {True or False}")
